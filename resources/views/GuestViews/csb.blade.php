                    <h3>CSB (Communication Skills for Business) </h3>
                    <p>CSB (Communication Skills for Business) est une nouvelle certification qui évalue les compétences
                        transversales telles que les compétences en communication. Les compétences en communication sont
                        essentielles au succès et sont l'une des caractéristiques les plus recherchées par les employeurs.
                        Le développement de ces compétences peut donner aux candidats un grand avantage dans leur recherche
                        d'emploi. La Certification a été réalisée par Certiport pour tester et valider des concepts et des
                        connaissances de communication dans un contexte commercial.</p>

                    <p>Les candidats au programme de formation et de certification CSB acquerront une connaissance
                        conceptuelle des principes commerciaux de l'entrepreneuriat, bien que les étudiants n'aient pas
                        besoin d'avoir une expérience préalable en gestion d'entreprise pour passer et réussir l'examen.</p>


                    <p>L'obtention de la certification CSB valide les compétences et les connaissances fondamentales en
                        communication dans un contexte commercial, ainsi que pour les aspirations professionnelles
                        spécifiques de l'entreprise. Qu'il s'agisse d'une entreprise informatique, d'un salon de beauté,
                        d'un magasin, d'un atelier ou plus, la certification CSB prépare les étudiants qui décident d'entrer
                        dans l'industrie des communications.</p>

                    <h3>Objectifs de l'examen</h3>

                    <p>La certification Communication Skills for Business (CSB) est conçue pour valider que les candidats
                        entrant dans le monde des affaires comprennent les principes de communication clés et les
                        compétences nécessaires pour être efficaces sur le lieu de travail. Les concepts fondamentaux
                        comprennent des compétences de communication professionnelles, des compétences verbales, non
                        verbales et d'écoute efficaces créant des résultats commerciaux ; transmettre efficacement un
                        message; recevoir des communications ; et analyser les scénarios de communication.</p>


