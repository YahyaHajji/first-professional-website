                    <p>L’entrepreneuriat et les petites entreprises est une certification de Certiport, parrainée par
                        Intuit, qui garantit que les dirigeants de demain sont préparés avec la boîte à outils dont ils ont
                        besoin pour avancer dans le paysage concurrentiel d’aujourd’hui. Que vous lanciez une boulangerie ou
                        que vous lanciez la prochaine startup d’un milliard de dollars, ESB valide la compréhension des
                        principes fondamentaux de l’entreprise par les étudiants, y compris les éléments essentiels
                        nécessaires pour lancer et maintenir une entreprise prospère.</p>
                    <p>Les candidats à la certification ESB devront avoir une connaissance conceptuelle clé des principes de
                        l’entrepreneuriat et des petites entreprises, bien qu’il ne soit pas nécessaire que les étudiants
                        aient une expérience du monde réel en tant que chef de petite entreprise pour passer et réussir
                        l’examen. La réussite de cette certification validera les compétences et les connaissances des
                        étudiants intéressés à travailler dans un métier de niveau intermédiaire comme leur propre patron et
                        ceux qui aspirent à l’entrepreneuriat et à la carrière dans une petite entreprise.</p>

