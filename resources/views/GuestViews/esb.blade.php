                    <h3>Pourquoi enseigner l’entrepreneuriat?</h3>
                    <p>« L’éducation à l’entrepreneuriat… enseigne aux enfants à sortir des sentiers battus et encourage les
                        talents et les compétences non conventionnels. De plus, il crée des opportunités, assure la justice
                        sociale, instaure la confiance et stimule l’économie. »</p>

                    <h3>Pourquoi les étudiants devraient-ils étudier et demander une certification en ESB?</h3>
                    <p>Qu’il s’agisse d’un salon de beauté dans une grande ville métropolitaine, d’un magasin de tacos dans
                        une station balnéaire en plein essor ou d’un garage de réparation automobile en banlieue, un nombre
                        incroyable de petites entreprises se trouvent presque partout. En fait, dans un récent rapport de
                        Business.com, « chaque minute une nouvelle entreprise est lancée aux États-Unis et selon certains,
                        plus de 50% de tous les travailleurs seront des travailleurs indépendants d’ici 2020 ». (La
                        situation des petites entreprises en Amérique, 2015, Business.com, je souligne.)</p>

                    <p>La certification ESB engage et prépare les étudiants qui poursuivront une formation postsecondaire
                        supplémentaire ou ceux qui choisissent d’entrer dans le secteur des petites entreprises
                        immédiatement après l’obtention du diplôme d’études secondaires. Les concepts entrepreneuriaux
                        validés par cette certification garantissent que ces étudiants sont prêts pour l’université et la
                        carrière.</p>

                    <h3>La solution complète pour la certification ESB</h3>
                    <p>Grâce à la solution de parcours complet de Certiport, vous pouvez préparer vos étudiants à la
                        certification ESB avec du matériel d’apprentissage personnalisé, des tests pratiques et un examen de
                        certification basé sur les performances officiellement approuvé par le Network for Teaching
                        Entrepreneurship (NFTE). L’ajout de ces matériaux dans votre programme est simple et transparent.
                    </p>

